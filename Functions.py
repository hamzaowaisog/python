# =============================================================================
# unknowns parameters
# =============================================================================

def function_2(*name):
    print("Hello " + name[0]+" and "+name[1])

function_2("Hamza","Haris")

# =============================================================================
# Default value in parameter
# =============================================================================

def function_3(name_1,name_2="Haris"):
    print("Hello "+name_1+" and "+name_2)
    
function_3("hamza")
function_3("Hamza","Danish")

